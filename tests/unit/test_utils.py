import sys
import os
import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "..", "src"))

from bot_stars.utils import decline_text_by_number, decline_stars_message, format_date


# ---------------------------------------------------------------------------
# decline_text_by_number
# ---------------------------------------------------------------------------

class TestDeclineTextByNumber:
    @pytest.mark.parametrize("n,expected", [
        # Форма «1» (единственное число)
        (1,  "яблоко"),
        (21, "яблоко"),
        (31, "яблоко"),
        (101, "яблоко"),
        # Форма «2–4»
        (2,  "яблока"),
        (3,  "яблока"),
        (4,  "яблока"),
        (22, "яблока"),
        (23, "яблока"),
        (34, "яблока"),
        # Форма «5+» (в т.ч. исключения 11–19)
        (5,  "яблок"),
        (6,  "яблок"),
        (10, "яблок"),
        (11, "яблок"),   # ключевое исключение — не «яблоко»
        (12, "яблок"),
        (13, "яблок"),
        (14, "яблок"),
        (19, "яблок"),
        (20, "яблок"),
        (100, "яблок"),
        (111, "яблок"),  # 11 в остатке
        (112, "яблок"),  # 12 в остатке
    ])
    def test_declension(self, n, expected):
        result = decline_text_by_number(n, "яблоко", "яблока", "яблок")
        assert result == expected, f"n={n}: ожидалось '{expected}', получили '{result}'"


# ---------------------------------------------------------------------------
# decline_stars_message
# ---------------------------------------------------------------------------

class TestDeclineStarsMessage:
    @pytest.mark.parametrize("n,expected", [
        (1,  "звезду"),
        (2,  "звезды"),
        (4,  "звезды"),
        (5,  "звёзд"),
        (11, "звёзд"),
        (12, "звёзд"),
        (21, "звезду"),
        (22, "звезды"),
        (25, "звёзд"),
    ])
    def test_star_form(self, n, expected):
        assert decline_stars_message(n) == expected

    def test_zero_stars(self):
        # 0 — это «много», форма «звёзд»
        assert decline_stars_message(0) == "звёзд"


