class TestFormatDate:
    @pytest.mark.parametrize("raw,expected", [
        ("2025-01-15 10:30:00", "15 января 2025"),
        ("2025-02-01 00:00:00", "01 февраля 2025"),
        ("2025-03-20 12:00:00", "20 марта 2025"),
        ("2025-04-05 08:00:00", "05 апреля 2025"),
        ("2025-05-09 00:00:00", "09 мая 2025"),
        ("2025-06-30 23:59:59", "30 июня 2025"),
        ("2025-07-04 00:00:00", "04 июля 2025"),
        ("2025-08-15 00:00:00", "15 августа 2025"),
        ("2025-09-01 00:00:00", "01 сентября 2025"),
        ("2025-10-31 00:00:00", "31 октября 2025"),
        ("2025-11-07 00:00:00", "07 ноября 2025"),
        ("2025-12-25 00:00:00", "25 декабря 2025"),
    ])
    def test_all_months(self, raw, expected):
        assert format_date(raw) == expected

    def test_invalid_format_raises(self):
        with pytest.raises(ValueError):
            format_date("не дата")
